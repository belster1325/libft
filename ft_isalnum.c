#include "libft.h"

int		ft_isalnum(int c)
{
	unsigned char k;

	k = (unsigned char)c;
	if ((c <= 90 && c >= 65) || (c <= 57 && c >= 48))
		return (1);
	else if (c <= 122 && c >= 97)
		return (1);
	else
		return (0);
}
